char	*ft_strupcase(char *str)
{
	int	i;

	if (str == 0)
		return (str);
	i = -1;
	while (str[++i])
	{
		if (str[i] >= 'a' && str[i] <= 'z')
			str[i] = str[i] - ('a' - 'A');
	}
	return (str);
}
