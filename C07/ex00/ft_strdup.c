#include <stdlib.h>

int	ft_strlen(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		i++;
	}
	return (i);
}

char	*ft_strdup(char *src)
{
	char	*res;
	int		len_src;
	int		i;

	len_src = ft_strlen(src);
	res = malloc(sizeof(*res) * len_src + 1);
	if (res == 0)
	{
		return (0);
	}
	i = 0;
	while (src[i])
	{
		res[i] = src[i];
		i++;
	}
	res[i] = '\0';
	return (res);
}
